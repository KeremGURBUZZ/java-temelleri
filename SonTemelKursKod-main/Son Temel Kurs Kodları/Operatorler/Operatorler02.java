package Operatorler;

public class Operatorler02 {

	public static void main(String[] args) {
		
		double x = 5;
		double y = 4;
		
		System.out.println("x ile y sayılarından büyük olan :"+ Math.max(x, y));
		System.out.println("x ile y sayılarından küçük olan :"+ Math.min(x, y));
		
		double pi = Math.PI;
		 
		System.out.println("Pİ değeri :"+ pi);
		System.out.println("y sayısının karekökü :"+ Math.sqrt(y));
		System.out.println("x sayısının y kuvveti :"+ Math.pow(x, y));

	}

}
