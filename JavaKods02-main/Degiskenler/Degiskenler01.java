package Degiskenler;

public class Degiskenler01 {

	public static void main(String[] args) {
	// byte küçük sayılar için ve fazla alan kaplamamak için kullanılır
		
		byte kucukSayi = 6;
		System.out.println(kucukSayi);
		
	// short biraz daha büyük sayılar için ve alandan tasarruf etmek için kullanılır
		
		short sayi = 32000;
		System.out.println(sayi);
		
	// genel olarak kullandığımız bir veri saklama değişkenidir 
		
		int genelSayi = 4500000;
		System.out.println(genelSayi);
		
	// long daha büyük sayılar için bunu kullanırız
		
		long buyukSayi = 500000000;
		System.out.println(buyukSayi);
		
	// double değişkeni ondalıklı sayılar için kullanılır
		
		double oran = 15.5;
		System.out.println(oran);
		
	// char tek bir karakter kullanmak istediğimizde char değişkenini kullanırız metinsel ifadelerde "" onun dışında ''
		
		char karakter = '#';
		System.out.println(karakter);
		
	// String metinsel ifadelerde bunu kullanırız S büyük ve ""; şeklinde yazılır
		
		String metin = "Kerem Gürbüz";
		System.out.println(metin);
	   

	}

}
