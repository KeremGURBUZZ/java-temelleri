package Donguler;

public class DoWhile {

	public static void main(String[] args) {
		//BELİRTİLEN KOD BLOĞU BİRKEZ CALISTIRILIR SONRA SARTI KONTROL EDER SART DOĞRU İSE TEKRAR EDER
		
		int caunt = 0;
		
		do {
			
			System.out.println("Caunt :" + caunt);
			
			caunt++;
		}while(caunt<5);
		

	}

}
